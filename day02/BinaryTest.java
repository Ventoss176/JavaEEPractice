/*
计算机中不同进制说明
*/
class BinaryTest {
	public static void main(String[] args) {
		int num1 = 0b110;//二进制
		int num2 = 110;//十进制
		int num3 = 0127;//八进制
		int num4 = 0x110A;//十六进制
		System.out.println("num1 = " + num1);
		System.out.println("num2 = " + num2);
		System.out.println("num3 = " + num3);
		System.out.println("num4 = " + num4);

	}
}
